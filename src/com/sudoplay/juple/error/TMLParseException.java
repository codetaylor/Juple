package com.sudoplay.juple.error;

/**
 * Thrown when a reader encounters a syntax error.
 * 
 * @author Jason Taylor
 */
public class TMLParseException extends RuntimeException {

  private static final long serialVersionUID = 6638851575346454047L;

  public TMLParseException() {
    super();
  }

  public TMLParseException(String message) {
    super(message);
  }

  public TMLParseException(Throwable throwable) {
    initCause(throwable);
  }

  public TMLParseException(String message, Throwable throwable) {
    super(message);
    initCause(throwable);
  }

}
